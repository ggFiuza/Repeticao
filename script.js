let x = 10;
console.log("While");
while(x>=1){
    console.log (x);
    x--; 
}

let z = 10;
console.log("Do-While");
do{
    console.log(z);
    z--;
}while (z >= 1);


let y = 10;
console.log("For");
for(y = 10;y>=1;y--){
    console.log(y);
}

